#!/usr/bin/env node
// One-shot enrichment: každé místo v src/lib/cz-locations.ts dostane lat/lng
// přes Nominatim OSM (free, rate-limit 1 req/s). Output → src/lib/cz-locations-geo.ts.
//
// Usage:
//   node scripts/enrich-cz-locations.mjs
//
// Nominatim TOS vyžaduje User-Agent s contact info. Free, ale slow (1 req/s).
// Pro 244 měst = ~4 minuty.

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const SRC = resolve(ROOT, 'src/lib/cz-locations.ts');
const OUT = resolve(ROOT, 'src/lib/cz-locations-geo.ts');
const USER_AGENT = 'agro-svet location enrichment (info@samecdigital.com)';

function parseLocations() {
  const raw = readFileSync(SRC, 'utf8');
  const m = raw.match(/export const CZ_LOCATIONS: string\[\] = \[([\s\S]*?)\];/);
  if (!m) throw new Error('Failed to parse CZ_LOCATIONS array');
  const items = [];
  for (const line of m[1].split('\n')) {
    const matches = line.matchAll(/'([^']+)'/g);
    for (const mm of matches) items.push(mm[1]);
  }
  return items;
}

async function geocode(name) {
  // Bias query to Czechia. Use structured query for cities, free-form for regions.
  const isRegion = name.includes('kraj') || name === 'Hlavní město Praha' || name === 'Vysočina';
  const params = new URLSearchParams({
    format: 'json',
    limit: '1',
    countrycodes: 'cz',
    'accept-language': 'cs',
  });
  if (isRegion) {
    params.set('q', name);
  } else {
    params.set('city', name);
  }
  const url = `https://nominatim.openstreetmap.org/search?${params}`;
  const r = await fetch(url, { headers: { 'User-Agent': USER_AGENT } });
  if (!r.ok) throw new Error(`HTTP ${r.status}`);
  const arr = await r.json();
  if (!Array.isArray(arr) || arr.length === 0) return null;
  return { lat: parseFloat(arr[0].lat), lng: parseFloat(arr[0].lon) };
}

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function main() {
  const items = parseLocations();
  console.log(`Enriching ${items.length} CZ locations…`);
  const results = [];
  let ok = 0;
  let fail = 0;
  for (let i = 0; i < items.length; i++) {
    const name = items[i];
    try {
      const geo = await geocode(name);
      if (geo) {
        results.push({ name, lat: geo.lat, lng: geo.lng });
        ok++;
        process.stdout.write(`  ✓ ${name} → ${geo.lat.toFixed(4)},${geo.lng.toFixed(4)}\n`);
      } else {
        results.push({ name, lat: null, lng: null });
        fail++;
        process.stdout.write(`  ✗ ${name} (no match)\n`);
      }
    } catch (e) {
      results.push({ name, lat: null, lng: null });
      fail++;
      process.stdout.write(`  ✗ ${name} (${e.message})\n`);
    }
    if (i < items.length - 1) await sleep(1100);
  }

  const tsLines = [
    '// Auto-generated by scripts/enrich-cz-locations.mjs — do not hand-edit.',
    '// Source: OpenStreetMap via Nominatim. ODbL license.',
    '// Regenerate when CZ_LOCATIONS in cz-locations.ts changes.',
    '',
    'export interface CzLocationGeo {',
    '  /** Display name as it appears in CZ_LOCATIONS. */',
    '  name: string;',
    '  /** WGS84 latitude. */',
    '  lat: number;',
    '  /** WGS84 longitude. */',
    '  lng: number;',
    '}',
    '',
    'export const CZ_LOCATIONS_GEO: CzLocationGeo[] = [',
    ...results
      .filter((r) => r.lat !== null && r.lng !== null)
      .map((r) => `  { name: ${JSON.stringify(r.name)}, lat: ${r.lat.toFixed(5)}, lng: ${r.lng.toFixed(5)} },`),
    '];',
    '',
    '/** Lookup by exact name (case-insensitive). Returns null if not found. */',
    'export function lookupCzLocation(name: string): CzLocationGeo | null {',
    '  const needle = name.trim().toLowerCase();',
    '  for (const loc of CZ_LOCATIONS_GEO) {',
    '    if (loc.name.toLowerCase() === needle) return loc;',
    '  }',
    '  return null;',
    '}',
    '',
  ].join('\n');
  writeFileSync(OUT, tsLines);

  console.log(`\n✓ ${ok} geocoded, ${fail} failed → ${OUT}`);
}

main().catch((e) => {
  console.error('Fatal:', e);
  process.exit(1);
});
